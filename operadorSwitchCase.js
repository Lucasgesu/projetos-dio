// estrutura de decisão
// switch/case/break/default
let fruta = "agua"
switch(fruta){
    case "maca": console.log("vitamina de maçã")
    break
    case "banana": console.log ("suco de banana")
    break
    case "laranja": console.log("fruta de laranja")
    break
    default: console.log("suco de limão")
}